package aula_02;

public class Soma {
	public static void main(String[] args) {
		
		int numero_01 = 2, numero_02 = 2;
		String numeroS_01 = "2", numeroS_02 = "2";
		
		System.out.println("A soma de inteiros é: " + (numero_01 + numero_02));
		System.out.println("A soma de strings é: " + (numeroS_01 + numeroS_02));
	}

}
